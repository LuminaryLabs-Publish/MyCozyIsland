import { createDeterministicSeedService } from "../kits/determinism.js";
import { createEnvironmentClock, createWindField, createWeatherState, createIlluminationState, createAerialPerspectiveDescriptor, createVegetationWindDescriptor } from "../kits/environment.js";
import { createTerrainSurface, createTerrainBiomeField, createShorelineField, createTerrainLodPolicy, createGroundContactService } from "../kits/terrain.js";
import { createOceanFloorProfile, createOceanWaveState, createOceanOpticsDescriptor, createShorelineFoamDescriptor, createUnderwaterAtmosphereDescriptor, createOceanCausticsDescriptor, createSunGlitterDescriptor } from "../kits/ocean.js";
import { createVegetationArchetypeCatalog, createVegetationPlacementGraph, createVegetationLodPolicy, createRockGraph, createPropGraph, createCampfireAtmosphereDescriptor } from "../kits/vegetation.js";
import { createCloudWeatherState, createCloudDensityRecipe, createCloudLightingProfile, createCloudLodPolicy, createCloudShadowDescriptor, createCloudHorizonBandDescriptor, createFogDensityRecipe, createFogAdvectionDescriptor, createFogVolumePlacement } from "../kits/atmosphere.js";
import { createStylizedMaterialCatalog, createRenderArchetypeCatalog, createRenderSnapshot, createWebGL2FallbackPolicy } from "../kits/render-descriptors.js";
import { createCameraRailSequence, createCozyIslandScenario } from "../kits/sequences.js";

export function createLegacyWorldComposition({ quality, backend = "webgpu" } = {}) {
  if (!quality) throw new TypeError("createLegacyWorldComposition requires a quality descriptor.");
  const seedService = createDeterministicSeedService("cozy-island-webgpu-v2");
  const clock = createEnvironmentClock({ initialSeconds: 48, timeScale: 1 });
  const windField = createWindField({ seedService, clock });
  const weather = createWeatherState({ preset: "sunrise-haze" });
  const illuminationService = createIlluminationState({ clock, weather });
  const illumination = illuminationService.getState();
  const terrain = createTerrainSurface({ seed: seedService.seed, radius: 108, maxHeight: 24, beachWidth: 12, clearingRadius: 17 });
  const terrainShelf = Object.freeze({ width: 6, depth: -5, handoffDepth: -7 });
  const biomeField = createTerrainBiomeField(terrain);
  const shoreline = createShorelineField(terrain);
  const terrainLod = createTerrainLodPolicy(quality);
  const oceanFloor = createOceanFloorProfile(terrain, { minimumCoastDepth: terrainShelf.handoffDepth });
  const oceanWaves = createOceanWaveState();
  const oceanOptics = createOceanOpticsDescriptor();
  const underwater = createUnderwaterAtmosphereDescriptor();
  const caustics = createOceanCausticsDescriptor();
  const sunGlitter = createSunGlitterDescriptor();
  const foam = createShorelineFoamDescriptor(terrain, { segments: quality.tier === "low" ? 220 : 320 });
  const vegetationArchetypes = createVegetationArchetypeCatalog();
  const groundContact = createGroundContactService(terrain);
  const vegetation = createVegetationPlacementGraph({ surface: terrain, biomeField, archetypes: vegetationArchetypes, groundContact, seed: `${seedService.seed}:vegetation`, qualityTier: quality.tier });
  const vegetationWind = createVegetationWindDescriptor(windField);
  const vegetationLod = createVegetationLodPolicy(quality);
  const rocks = createRockGraph({ surface: terrain, groundContact, seed: `${seedService.seed}:rocks`, qualityTier: quality.tier });
  const props = createPropGraph(terrain, `${seedService.seed}:props`);
  const campfire = createCampfireAtmosphereDescriptor(terrain, windField);
  const cloudWeather = createCloudWeatherState(weather, windField);
  const cloudDensity = createCloudDensityRecipe(cloudWeather, quality);
  const cloudLighting = createCloudLightingProfile(illuminationService);
  const cloudLod = createCloudLodPolicy(quality);
  const cloudShadow = createCloudShadowDescriptor(cloudWeather, cloudLod);
  const cloudHorizon = createCloudHorizonBandDescriptor(cloudWeather);
  const fogDensity = createFogDensityRecipe(weather, quality);
  const fogAdvection = createFogAdvectionDescriptor(windField);
  const fogPlacement = createFogVolumePlacement(fogDensity);
  const aerialPerspective = createAerialPerspectiveDescriptor();
  const materials = createStylizedMaterialCatalog();
  const renderArchetypes = createRenderArchetypeCatalog();
  const fallbackPolicy = createWebGL2FallbackPolicy();
  const descriptors = Object.freeze({ seed: seedService.seed, quality, backend, terrain, terrainShelf, terrainLod, biomeField, shoreline, oceanFloor, oceanWaves, oceanOptics, underwater, caustics, sunGlitter, foam, vegetation, vegetationArchetypes, vegetationWind, vegetationLod, rocks, props, campfire, cloudWeather, cloudDensity, cloudLighting, cloudLod, cloudShadow, cloudHorizon, fogDensity, fogAdvection, fogPlacement, illumination, aerialPerspective, materials, renderArchetypes, fallbackPolicy });
  const snapshot = createRenderSnapshot(descriptors);
  const cameraSequence = createCameraRailSequence(terrain);
  const scenario = createCozyIslandScenario({ clock, cameraSequence, snapshot });
  return Object.freeze({ ...descriptors, snapshot, scenario, cameraSequence, clock, windField, weather, illuminationService, seedService, groundContact });
}
